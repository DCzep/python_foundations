vist_list = ['maldives', 'norway', 'banff', 'new zealand', 'oregon']
print(vist_list)
# Print Sorted List
print(sorted(vist_list))
# Print Original List in original order
print(vist_list)
# Reverse Order Print
vist_list.reverse()
print(vist_list)
# Use Reverse Again to Return to Original Order
vist_list.reverse()
print(vist_list)
# Sort list in alphabetical order
vist_list.sort()
print(vist_list)
# Sort again so it's stored in reverse-alphabeetically order
vist_list.sort(reverse=True)
print(vist_list)